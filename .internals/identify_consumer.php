public static function identify ()
{
	// Загружаем или начинаем сессию для посетителя, загружаем данные сессии,
	// отправляем посетителю cookie с id сессии (если это новая сессия).
//	session_name(_config_::$session_cookie_name);
//	session_set_cookie_params(_config_::$session_cookie_lifetime, _config_::$session_cookie_path, _config_::$session_cookie_domain);
//	session_start();

		self::$info = $result = null;
		
	// Для пользовательской сессии читаем данные пользователя из базы.
	//!!!todo: для скорости работы лучше сделать хранение данных в самой сессии,
	//!!!todo: и перечитывать их при операциях изменения. но мало ли что там поменяется в базе,
	//!!!todo: и надо отразить прямо в процессе работы сессии.
	if (isset($_GET['auth']) && @$_SESSION['@id'] == 1)
	{
		$tmp = explode('|', $_GET['auth']);
		$id = $tmp[0];
		$encrypt_pwd = $tmp[1];

		// Читаем все поля из базы.
		$dat = _main_::query(null, "
			select	id
			from	`users`
			where	`id` = {1} and `encrypted_password` = {2}",
			$id, $encrypt_pwd);

		if ($dat)
		{
			$_SESSION['@id'] = array_shift($dat)['id'];
		}
	}

	if (isset($_SESSION['@id']))
	{
		// Читаем все поля из базы.
		$dat = _main_::query(null, "
			select	*
			from	`users`
			where	`id` = {1}",
			$_SESSION['@id']);

		// Чистим от ненужных полей для пущей паранойи (чтобы не засветить хеши в DOM-дампе).
		foreach ($dat as $idx => $row)
			unset($dat[$idx]['encrypted_password']);
		//!!!fix: когда пользователь стёрт из базы (т.е. !count($visitor_info)), но его ид осталось в сессии,
		//!!!fix: мы его ниже игнорируем и считаем анонимом, НО надо очистить сессию от user-переменных.

		$m = _main_::fetchModule('users');
		$m->data_parce($dat);

		self::$info = $result = array_shift($dat);
		
		$m = _main_::fetchModule('users/log');
		$m->log_request();
	} else
	{
		self::$info = $result = null;
	}


	return $result;
}
